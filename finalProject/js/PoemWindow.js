/****************************************************************************/
                            //POEM WINDOW OBJECT

// the window on the canvas in which the user can make beautiful poems
/****************************************************************************/

/****************************************************************************/
                              //CONSTRUCTOR
/****************************************************************************/

function PoemWindow(x,y,w,h) {
  this.x = x;
  this.y = y;
  this.w = w;
  this.h = h;
}

/****************************************************************************/
                            //DISPLAY FUNCTION
/****************************************************************************/

PoemWindow.prototype.display = function() {
  rectMode(CENTER);
  fill(255);
  noStroke();
  rect(this.x,this.y,this.w,this.h);
}

